    public void showData(List<sanPhamChiTietModel> spCTs) {
        dtm.setRowCount(0);
        for (sanPhamChiTietModel sp : spCTs) {
            Object[] row = new Object[]{
                sp.getIdctsp(), sp.getIdsp(), sp.getIdncc(),
                sp.getIdsp(), sp.getIdMauSac(), sp.getIdSize(),
                sp.getGiaNhap(), sp.getGiaBan(), sp.getChatLieu(), sp.getSlTon(),
                sp.getNgayLap(), sp.getNgaySua(), sp.isTrangThai() == true ? "Còn hàng":"Không còn hàng"
            };
            dtm.addRow(row);
        }
    }
