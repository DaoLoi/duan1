    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        sanPhamChiTietModel spCT = new sanPhamChiTietModel();
        if (txtGiaNhap.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Trống giá nhập");
            return;
        }
        if (txtGiaBan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Trống giá bán");
            return;
        }
        if (txtChatLieu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Trống chất liệu");
            return;
        }
        if (txtSoLuongTon.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Trống số lượng tồn");
            return;
        }
        if (txtNgayLap.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Trống ngày lập");
            return;
        }
        if (txtNgaySua.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Trống ngày sửa");
            return;
        }
        spCT.setGiaNhap(new BigDecimal(txtGiaNhap.getText()));
        spCT.setGiaBan(new BigDecimal(txtGiaBan.getText()));
        spCT.setChatLieu(txtChatLieu.getText());
        spCT.setSlTon(Integer.parseInt(txtSoLuongTon.getText()));
        spCT.setNgayLap(txtNgayLap.getText());
        spCT.setNgaySua(txtNgaySua.getText());
        if (rbConHang.isSelected()) {
            spCT.setTrangThai(true);
        }
        if (rbKhongConHang.isSelected()) {
            spCT.setTrangThai(false);
        }
        if (spCTService.add(spCT)) {
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            showData(spCTService.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
            return;
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tbSPCTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSPCTMouseClicked
        // TODO add your handling code here:
        int index = tbSPCT.getSelectedRow();
        List<sanPhamChiTietModel> spCTs = spCTService.getAll();
        sanPhamChiTietModel spCT = spCTs.get(index);
        txtCTSP.setText(spCT.getIdctsp());
        txtSP.setText(spCT.getIdsp());
        txtNCC.setText(spCT.getIdncc());
        txtLSP.setText(spCT.getIdlsp());
        txtMS.setText(spCT.getIdMauSac());
        txtSize.setText(spCT.getIdSize());
        
        txtGiaNhap.setText(spCT.getGiaNhap().toString());
        txtGiaBan.setText(spCT.getGiaBan().toString());
        txtChatLieu.setText(spCT.getChatLieu());
        txtSoLuongTon.setText(String.valueOf(spCT.getSlTon()));
        txtNgayLap.setText(spCT.getNgayLap());
        txtNgaySua.setText(spCT.getNgaySua());
        if (spCT.isTrangThai()== true) {
            rbKhongConHang.setSelected(false);
            rbConHang.setSelected(true);
        } else {
            rbKhongConHang.setSelected(true);
            rbConHang.setSelected(false);
        }
    }//GEN-LAST:event_tbSPCTMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         int index = tbSPCT.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn để sửa!");
            return;
        }
        List<sanPhamChiTietModel> spCTs = spCTService.getAll();
        sanPhamChiTietModel spCT = spCTs.get(index);
        
        spCT.setGiaNhap(new BigDecimal(txtGiaNhap.getText()));
        spCT.setGiaBan(new BigDecimal(txtGiaBan.getText()));
        spCT.setChatLieu(txtChatLieu.getText());
        spCT.setSlTon(Integer.valueOf(txtSoLuongTon.getText()));
        spCT.setNgayLap(txtNgayLap.getText());
        spCT.setNgaySua(txtNgaySua.getText());
        if (rbConHang.isSelected()) {
            spCT.setTrangThai(true);
        }
        if (rbKhongConHang.isSelected()) {
            spCT.setTrangThai(false);
        }
        String idCTSP = txtCTSP.getText();
        try {
            spCTService.update(spCT, idCTSP);
            JOptionPane.showMessageDialog(this, "Sửa thành công!");
            showData(spCTService.getAll());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sửa thất bại!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = tbSPCT.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn để xóa!");
            return;
        }
        List<sanPhamChiTietModel> spCTs = spCTService.getAll();
        sanPhamChiTietModel spCT = spCTs.get(index);
        String idCTSP = txtCTSP.getText();
        int kq = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa?", "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.YES_OPTION);
        if (kq == JOptionPane.YES_OPTION) {
            try {
                spCTService.delete(idCTSP);
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
                showData(spCTService.getAll());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Xóa thất bại!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

